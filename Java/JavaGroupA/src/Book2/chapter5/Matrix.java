package Book2.chapter5;

public class Matrix {
    public static void main(String[] args) {
        int x;
        int y;

        for (x = 1; x < 10; x++) {
            for (y = 1; y < 10; y++) {
                System.out.print(x + "-" + y + " ");
            }
            System.out.println();
        }
    }
}
